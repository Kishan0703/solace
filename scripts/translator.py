def process_file(filepath):
    if not os.path.exists(filepath):
        return
    print(f"Processing {filepath}")
    with open(filepath, "r", encoding="utf-8") as f:
        content = f.read()
    
    # Find all contiguous Chinese strings (including punctuation like ，。！？、)
    # We will use a regex that matches Chinese characters and common Chinese punctuation
    # \u4e00-\u9fa5 is Han characters. \u3000-\u303f are CJK symbols. \uff01-\uff5e are fullwidth forms.
    pattern = re.compile(r"[\u4e00-\u9fa5\u3000-\u303f\uff01-\uff5e]+")
    
    unique_strings = set(pattern.findall(content))
    # print(f"Found {len(unique_strings)} unique Chinese strings in {filepath}")
    
    translation_cache = {}
    for s in unique_strings:
        # Some strings might just be punctuation, skip if no Han character
        if not re.search(r"[\u4e00-\u9fa5]", s):
            continue
            
        translated = translate_with_gemini(s)
        translation_cache[s] = translated
        time.sleep(0.5) # rate limit
        
    # Replace in content (longest strings first to avoid partial replacements)
    sorted_strings = sorted(translation_cache.keys(), key=len, reverse=True)
    for s in sorted_strings:
        content = content.replace(s, translation_cache[s])
        
    with open(filepath, "w", encoding="utf-8") as f:
        f.write(content)
    print(f"Done {filepath}")

for f in files_to_translate:
    process_file(f)
