import urllib.request
import urllib.parse
import json
import time

def translate_with_gemini(text):
    if text in mapping:
        return mapping[text]
    
    # Avoid translating python code parts that accidentally matched, 
    # but the regex will only match Chinese.
    
    api_key = os.environ.get("GEMINI_API_KEY")
    if not api_key:
        print("No API key, skipping", text[:20])
        return text
        
    url = f"https://generativelanguage.googleapis.com/v1beta/models/gemini-2.5-flash:generateContent?key={api_key}"
    payload = {
        "contents": [{"parts": [{"text": f"Translate this Chinese text to English. Return ONLY the English translation, no explanation, no quotes:\n\n{text}"}]}],
        "generationConfig": {"temperature": 0.1, "maxOutputTokens": 2048}
    }
    
    req = urllib.request.Request(url, data=json.dumps(payload).encode('utf-8'), headers={'Content-Type': 'application/json'})
    try:
        with urllib.request.urlopen(req) as response:
            res_data = json.loads(response.read().decode())
            translation = res_data['candidates'][0]['content']['parts'][0]['text'].strip()
            # print(f"Translated: {text[:20]} -> {translation[:20]}")
            return translation
    except Exception as e:
        print("Error translating:", e)
        return text

